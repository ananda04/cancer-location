function lbl=test_ann(fig, epsilon, minpts, dist, dtl)
left=.041;
bottom=.2;
width=.41;
height=.125;
info=['detail="' dtl '", epsilon=' num2str(epsilon)];
info2=['minpts=' num2str(minpts) ', dbscan distance="' dist '"'];
lbl=annotation(fig, 'textbox','String', ...
    {['\color{blue} '...
    info], [ info2]}, 'units', 'normalized', ...
    'position', [left bottom width height], 'fontSize', 11, ...
    'HorizontalAlignment', 'center');
end